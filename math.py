def add(a, b):
    """
    Additionne deux nombres et retourne le résultat.
    
    Args:
        a: Le premier nombre
        b: Le deuxième nombre
    
    Returns:
        La somme de a et b
    """
    return a + b


def sub(a, b):
    """
    Soustrait deux nombres et retourne le résultat.
    
    Args:
        a: Le premier nombre
        b: Le deuxième nombre
    
    Returns:
        La différence entre a et b
    """
    return a - b
